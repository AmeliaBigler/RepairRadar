const winnerBid = async (req, res, ticketData) => {
    try {
        //Find the mechanic who won the bid
        const mechanicData = await Mechanic.findByPk(ticketData.winner)

        //Get the data from the table
        const mechanic = await mechanicData.get({ plain: true });

        //Set up the nodemailer
        let config = {
            service: "gmail",
            auth: {
                user: process.env.EMAIL_USER,
                pass: process.env.EMAIL_PASSWORD
            }
        };

        //Initialize nodemailer
        let transporter = nodemailer.createTransport(config);

        //Initialize mailgen
        let mailGenerator = new mailgen({
            theme: "salted",
            product: {
                name: "RepairRadar",
                //TODO: Add the link to the deployed site
                link: "https://damp-oasis-29946.herokuapp.com"
            }
        });

        //Make the email
        let response = {
            body: {
                name: req.body.username,
                intro: "You've Won!",
                action: {
                    instructions: "One of the tickets you bid on has declared you as the winner! Click below to see new messages!",
                    button: {
                        text: "Go to messages",
                        //TODO: Add link to dashboard on the deployed site
                        link: "https://damp-oasis-29946.herokuapp.com/messages"
                    }
                },
                outro: "This is an autogenerated email from an unmonitored email inbox. Please do not reply to this email"
            }
        };

        //Generate the HTML and the plain text
        let mail = mailGenerator.generate(response);
        let mailtext = mailGenerator.generatePlaintext(response);

        //Send the email
        transporter.sendMail({
            from: `RepairRadar <${process.env.EMAIL_USER}>`,
            to: mechanic.email,
            subject: "Bid Winner!",
            text: mailtext,
            html: mail
        }).then(async () => {
            const userData = await User.findByPk(req.session.user_id, {
                include: [{
                    model: Ticket,
                    include: [{
                        model: Bids,
                        include: {
                            model: Mechanic,
                            model: Ticket
                        }
                    }, {
                        model: Mechanic,
                    }
                    ]
                }]
            });
            let user = userData.get({ plain: true });
            user = await user;
            res.render("dashboard", {
                user: user,
                logged_in: req.session.logged_in,
                isMechanic: req.session.isMechanic
            });
        })
    } catch (error) {
        res.status(500).json(error);
    }
}

module.exports = { signup: signup, winnerBid: winnerBid };